<?php


namespace NicAPI\Manager\Traits;


trait TransformTraits
{

    public function processResponse($response)
    {
        $response = $response->getBody()->__toString();
        $result = json_decode($response);
        if (json_last_error() == JSON_ERROR_NONE) {
            return $result;
        } else {
            return $response;
        }
    }

}